#define LEN(x)	(sizeof (x) / sizeof (x[0]))

static uint32_t *
rbsearch(uint32_t c, uint32_t *t, int n, int ne)
{
	uint32_t *p;
	int m;

	while (n > 1) {
		m = n >> 1;
		p = t + m * ne;

		if (c >= p[0]) {
			t = p;
			n = n - m;
		} else
			n = m;
	}

	if (n && c >= t[0])
		return t;
	return 0;
}

